public class KeywordsExample {
    
    public static void main(String[] args){
        //Constants: constants are the keywords whose values cannot be reassigned
        double VALUE_OF_PI = 3.14;
        // valueOfPI = 5.67;
        System.out.println(VALUE_OF_PI);

        // Identifiers:
        int age = 40; //age is an identifier
        String name = "Alpha"; // name is an identifier

        //Literals:
        int firstNumber = 40; //Integer literal
        boolean isActive = false; //boolean literal
    }
}
